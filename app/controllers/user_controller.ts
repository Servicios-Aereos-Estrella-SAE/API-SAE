  async login({ request, response }: HttpContext) {
    try {
      const userEmail = request.input('userEmail')
      const userPassword = request.input('userPassword')
      const user = await User.query().where('user_email', userEmail).where('user_active', 1).first()
      if (!user) {
        response.status(404)
        return {
          type: 'warning',
          title: 'Login',
          message: 'Incorrect email or password',
          data: { user: {} },
        }
      }
      await ApiToken.query().where('tokenable_id', user.userId).delete()
      const userVerify = await User.verifyCredentials(userEmail, userPassword)
      const token = await User.accessTokens.create(user)
      if (userVerify && token) {
        response.status(200)
        return {
          type: 'success',
          title: 'Login',
          message: 'You have successfully logged in',
          data: {
            user: user,
            token: token.value!.release(),
          },
        }
      } else {
        response.status(404)
        return {
          type: 'warning',
          title: 'Login',
          message: 'Incorrect email or password',
          data: { user: {} },
        }
      }
    } catch (error) {
      response.status(500)
      return {
        type: 'error',
        title: 'Server error',
        message: 'An unexpected error has occurred on the server',
        error: error.message,
      }
    }
  }

